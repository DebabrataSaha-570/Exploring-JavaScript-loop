// while loop

var kanDhor = 0;

while (kanDhor <= 7) {
    console.log('ami ar beyadopi korbo na boss');
    kanDhor++;
}

// for loop 
for (kanDhor = 0; kanDhor <= 10; kanDhor++) {
    console.log('ami ar beyadopi korbo na boos')
}

// odd number 
for (i = 1; i <= 20; i += 2) {
    console.log(i)
}

// even number 
for (i = 0; i <= 20; i += 2) {
    console.log(i)
}